#!/usr/bin/env python3
"""Explore the degree-18 three-branch conductor equalizer on finite jets.

For a chosen normalization-jet order, this script constructs the subalgebra
of the direct sum of the three smooth branch jets generated by the normalized
seed coefficients.  It then computes the three branch-prime kernels and the
distributivity-defect space

    ((p1+p3) intersect (p2+p3)) / (p3 + (p1 intersect p2)).

Vanishing is necessary for pairwise conductor data to recover the completed
component ring.  A finite-jet calculation is evidence, not yet an all-order
proof, so this script intentionally remains under the ``explore_`` prefix.
"""

import argparse
import itertools

import sympy as sp
from sympy.polys.matrices import DomainMatrix


W, tau = sp.symbols("W tau")
roots = (sp.Rational(-3, 4), sp.Integer(1), sp.Integer(2))
deltas = sp.symbols("delta0:7")


def truncate(expression, variables, order):
    """Discard monomials of total degree greater than ``order``."""
    polynomial = sp.Poly(sp.expand(expression), *variables)
    return sp.Add(
        *(
            coefficient * sp.prod(variable**exponent for variable, exponent in zip(variables, monomial))
            for monomial, coefficient in polynomial.terms()
            if sum(monomial) <= order
        )
    )


def incidence(polynomial):
    return (
        polynomial.subs(W, 1)
        - polynomial.subs(W, 0)
        - sp.diff(polynomial, W).subs(W, 0)
    )


def denominator(polynomial):
    return sp.diff(polynomial, W).subs(W, 1) - sp.diff(polynomial, W).subs(W, 0)


def branch_jets(q_root, order):
    """Return the seventeen normalized coefficient jets on one smooth sheet."""
    r_roots = list(roots)
    r_roots.remove(q_root)
    point_q = sp.expand((W - q_root) ** 3)
    point_r = sp.expand(sp.prod((W - root) ** 2 for root in r_roots))
    delta_q = deltas[2] * W**2 + deltas[1] * W + deltas[0]
    delta_r = (
        deltas[6] * W**3
        + deltas[5] * W**2
        + deltas[4] * W
        + deltas[3]
    )
    local_product = sp.Poly(
        sp.expand((point_q + tau * delta_q) ** 2 * (point_r + tau * delta_r) ** 3),
        tau,
    )
    product_parts = tuple(
        sp.expand(local_product.coeff_monomial(tau**degree))
        for degree in range(6)
    )
    phi_parts = tuple(sp.expand(incidence(part)) for part in product_parts)
    d_parts = tuple(sp.expand(denominator(part)) for part in product_parts)
    numerator_parts = tuple(
        tuple(
            -sp.Poly(part, W).coeff_monomial(W**degree)
            for degree in range(2, 19)
        )
        for part in product_parts
    )
    assert phi_parts[0] == 0
    assert d_parts[0] != 0

    constraint = sp.Matrix(
        [[sp.diff(phi_parts[1], delta) for delta in deltas]]
    )
    tangent = sp.Matrix.hstack(*constraint.nullspace())
    assert tangent.shape == (7, 6)
    pivot = next(index for index, entry in enumerate(constraint.tolist()[0]) if entry)

    variables = sp.symbols(f"x_{roots.index(q_root)}_0:6")
    variable_vector = sp.Matrix(variables)
    linear_delta = tangent * variable_vector
    correction = sp.Integer(0)
    phi_full = sum(phi_parts)
    pivot_derivative = constraint[0, pivot]
    for _ in range(order):
        substitution = {
            deltas[index]: linear_delta[index] + (correction if index == pivot else 0)
            for index in range(7)
        }
        residual = truncate(phi_full.subs(substitution), variables, order)
        correction = truncate(
            correction - residual / pivot_derivative, variables, order
        )
    substitution = {
        deltas[index]: linear_delta[index] + (correction if index == pivot else 0)
        for index in range(7)
    }
    assert truncate(phi_full.subs(substitution), variables, order) == 0

    d_series = truncate(sum(d_parts).subs(substitution), variables, order)
    d_positive = truncate(d_series - d_parts[0], variables, order)
    inverse_d = sp.Integer(0)
    ratio = truncate(-d_positive / d_parts[0], variables, order)
    power = sp.Integer(1)
    for _ in range(order + 1):
        inverse_d += power / d_parts[0]
        power = truncate(power * ratio, variables, order)
    inverse_d = truncate(inverse_d, variables, order)
    assert truncate(d_series * inverse_d, variables, order) == 1

    jets = []
    for coefficient in range(17):
        numerator = truncate(
            sum(parts[coefficient] for parts in numerator_parts).subs(substitution),
            variables,
            order,
        )
        normalized = truncate(numerator * inverse_d, variables, order)
        jets.append(truncate(normalized - normalized.subs({v: 0 for v in variables}), variables, order))
    return variables, tuple(jets)


def exponent_basis(variable_count, order):
    return tuple(
        exponent
        for total in range(order + 1)
        for exponent in itertools.product(range(total + 1), repeat=variable_count)
        if sum(exponent) == total
    )


def polynomial_dict(expression, variables, order):
    return {
        monomial: coefficient
        for monomial, coefficient in sp.Poly(sp.expand(expression), *variables).terms()
        if sum(monomial) <= order and coefficient
    }


def multiply_polynomials(left, right, order):
    result = {}
    for left_exponent, left_coefficient in left.items():
        for right_exponent, right_coefficient in right.items():
            exponent = tuple(a + b for a, b in zip(left_exponent, right_exponent))
            if sum(exponent) > order:
                continue
            result[exponent] = result.get(exponent, 0) + left_coefficient * right_coefficient
    return {exponent: coefficient for exponent, coefficient in result.items() if coefficient}


def multiply_branch_tuples(left, right, order):
    return tuple(
        multiply_polynomials(left[index], right[index], order)
        for index in range(3)
    )


def column_space(matrix):
    if matrix.cols == 0:
        return sp.zeros(matrix.rows, 0)
    # DomainMatrix uses exact polynomial-domain linear algebra and avoids the
    # severe rational swell of Matrix.columnspace on the quadratic jet.
    reduced, pivots = DomainMatrix.from_Matrix(matrix.T).rref()
    if not pivots:
        return sp.zeros(matrix.rows, 0)
    row_basis = reduced.to_Matrix()[: len(pivots), :]
    return row_basis.T


def sum_space(left, right):
    return column_space(left.row_join(right))


def intersection_space(left, right):
    if left.cols == 0 or right.cols == 0:
        return sp.zeros(left.rows, 0)
    relations = left.row_join(-right).nullspace()
    if not relations:
        return sp.zeros(left.rows, 0)
    vectors = [left * relation[: left.cols, :] for relation in relations]
    return column_space(sp.Matrix.hstack(*vectors))


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--order", type=int, default=2, choices=(1, 2, 3))
    args = parser.parse_args()
    order = args.order

    branches = [branch_jets(root, order) for root in roots]
    basis = exponent_basis(6, order)
    basis_index = {exponent: index for index, exponent in enumerate(basis)}
    branch_dimension = len(basis)
    zero_exponent = (0,) * 6
    one_polynomial = {zero_exponent: sp.Integer(1)}
    unit = (one_polynomial, one_polynomial, one_polynomial)
    generators = tuple(
        tuple(
            polynomial_dict(branches[branch][1][coefficient], branches[branch][0], order)
            for branch in range(3)
        )
        for coefficient in range(17)
    )

    algebra_vectors = [unit]
    for degree in range(1, order + 1):
        for indices in itertools.combinations_with_replacement(range(17), degree):
            product = unit
            for index in indices:
                product = multiply_branch_tuples(product, generators[index], order)
            algebra_vectors.append(product)

    entries = {}
    for column, vector in enumerate(algebra_vectors):
        for branch in range(3):
            for exponent, coefficient in vector[branch].items():
                row = branch * branch_dimension + basis_index[exponent]
                entries[(row, column)] = coefficient
    generated = sp.MutableSparseMatrix(
        3 * branch_dimension, len(algebra_vectors), entries
    ).as_immutable()
    algebra = column_space(generated)
    algebra_dimension = algebra.cols

    branch_kernels = []
    for branch in range(3):
        projection = algebra[
            branch * branch_dimension : (branch + 1) * branch_dimension, :
        ]
        kernel = projection.nullspace()
        branch_kernels.append(
            sp.Matrix.hstack(*kernel) if kernel else sp.zeros(algebra_dimension, 0)
        )

    p1, p2, p3 = branch_kernels
    numerator = intersection_space(sum_space(p1, p3), sum_space(p2, p3))
    denominator_space = sum_space(p3, intersection_space(p1, p2))
    assert sum_space(numerator, denominator_space).cols == numerator.cols
    defect_dimension = numerator.cols - denominator_space.cols

    print("normalization jet order:", order)
    print("dimension of one smooth branch jet:", branch_dimension)
    print("dimension of generated coefficient algebra:", algebra_dimension)
    print("branch-prime kernel dimensions:", tuple(kernel.cols for kernel in branch_kernels))
    print("distributivity numerator dimension:", numerator.cols)
    print("distributivity denominator dimension:", denominator_space.cols)
    print("distributivity-defect dimension:", defect_dimension)
    if defect_dimension == 0:
        print(f"PASS: the three-branch conductor equalizer has no defect through order {order}")
    else:
        print(f"WITNESS: a conductor equalizer defect appears through order {order}")


if __name__ == "__main__":
    main()
