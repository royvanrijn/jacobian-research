#!/usr/bin/env python3
"""Exact bounded audit of the quadratic-remainder tangent-cone proposal.

The calculation tests one specific assertion: that the transverse initial
ideal of the square/cube transfer block is generated by the coefficients of
rem_S(A^2).  It does not search for an all-k Groebner pattern.

For k <= 6 the script checks:

* the degree-two components of the displayed coefficient equations generate
  the quadratic remainder ideal;
* the collided transfer algebra is zero-dimensional of length 2^k;
* the collided quadratic remainder algebra has the positive-dimensional
  family A divisible by Z^ceil(k/2), so it cannot be that tangent cone; and
* at k=2 the missing initial relation is X^3.

All arithmetic is exact over QQ.  The bound is part of the result.
"""
from __future__ import annotations

import argparse
from collections import defaultdict
from itertools import product

import sympy as sp


Z = sp.Symbol("Z")
t = sp.Symbol("t")


def presentation(k: int, collided: bool):
    base = sp.symbols(f"s1:{k + 1}")
    a = sp.symbols(f"a0:{k}")
    coefficients = (sp.Integer(0),) * k if collided else base
    S = Z**k + sum(coefficients[j - 1] * Z ** (k - j) for j in range(1, k + 1))
    A = sum(a[j] * Z**j for j in range(k))
    Q_poly, R_poly = sp.div(sp.Poly(A**2, Z), sp.Poly(S, Z))
    Q, R = sp.expand(Q_poly.as_expr()), sp.expand(R_poly.as_expr())
    F = sp.Poly(sp.expand(9 * Q**2 + 8 * S * A * Q - 48 * S**2 * R - 64 * A * R), Z)
    equations = tuple(sp.expand(c) for c in F.all_coeffs() if c != 0)
    remainders = tuple(sp.expand(R.coeff(Z, j)) for j in range(k))
    return base, a, S, A, Q, R, equations, remainders


def transverse_order(f: sp.Expr, variables: tuple[sp.Symbol, ...]) -> int:
    return min(sum(monomial) for monomial, _ in sp.Poly(f, *variables).terms())


def initial_form(f: sp.Expr, variables: tuple[sp.Symbol, ...]) -> sp.Expr:
    polynomial = sp.Poly(f, *variables)
    order = min(sum(monomial) for monomial, _ in polynomial.terms())
    return sp.expand(sum(
        coefficient * sp.prod(variable**power for variable, power in zip(variables, monomial))
        for monomial, coefficient in polynomial.terms()
        if sum(monomial) == order
    ))


def homogeneous_component(
    f: sp.Expr, variables: tuple[sp.Symbol, ...], degree: int
) -> sp.Expr:
    polynomial = sp.Poly(f, *variables)
    return sp.expand(sum(
        coefficient * sp.prod(variable**power for variable, power in zip(variables, monomial))
        for monomial, coefficient in polynomial.terms()
        if sum(monomial) == degree
    ))


def reduced_basis(expressions, variables, domain=sp.QQ):
    return sp.groebner(expressions, *variables, order="grevlex", domain=domain)


def same_ideal(left, right, variables, domain=sp.QQ) -> bool:
    gl = reduced_basis(left, variables, domain)
    gr = reduced_basis(right, variables, domain)
    return (
        all(gr.reduce(f)[1] == 0 for f in left)
        and all(gl.reduce(f)[1] == 0 for f in right)
    )


def leading_exponents(basis: sp.GroebnerBasis):
    return tuple(poly.LM(order=basis.order).exponents for poly in basis.polys)


def standard_exponents_zero_dimensional(basis: sp.GroebnerBasis):
    leading = leading_exponents(basis)
    zero = (0,) * len(basis.gens)
    seen, stack = {zero}, [zero]
    while stack:
        exponent = stack.pop()
        for j in range(len(exponent)):
            neighbor = list(exponent)
            neighbor[j] += 1
            neighbor = tuple(neighbor)
            if neighbor in seen:
                continue
            if any(all(x >= y for x, y in zip(neighbor, lead)) for lead in leading):
                continue
            seen.add(neighbor)
            stack.append(neighbor)
            if len(seen) > 100_000:
                raise AssertionError("quotient did not appear zero-dimensional")
    return tuple(sorted(seen, key=lambda exponent: (sum(exponent), exponent)))


def divides(left, right):
    return all(x <= y for x, y in zip(left, right))


def monomial_hilbert_series(basis: sp.GroebnerBasis) -> sp.Expr:
    """Hilbert series from the minimal monomial generators of in(I)."""
    leading = list(leading_exponents(basis))
    leading = [
        g for g in leading
        if not any(h != g and divides(h, g) for h in leading)
    ]
    terms: dict[int, int] = defaultdict(int)
    for mask in range(1 << len(leading)):
        lcm = (0,) * len(basis.gens)
        parity = 0
        for j, generator in enumerate(leading):
            if mask & (1 << j):
                lcm = tuple(max(x, y) for x, y in zip(lcm, generator))
                parity ^= 1
        terms[sum(lcm)] += -1 if parity else 1
    numerator = sum(coefficient * t**degree for degree, coefficient in terms.items())
    return sp.factor(sp.cancel(numerator / (1 - t) ** len(basis.gens)))


def format_monomial(exponent, variables):
    return str(sp.prod(variable**power for variable, power in zip(variables, exponent)))


def audit(maximum: int) -> None:
    if maximum > 6:
        raise ValueError("this targeted regression is intentionally capped at k=6")

    print("quadratic-remainder transverse-initial audit over QQ")
    for k in range(1, maximum + 1):
        _, a, _, _, _, _, equations, remainders = presentation(k, collided=True)
        degree_two = tuple(
            part for f in equations
            if (part := homogeneous_component(f, a, 2)) != 0
        )
        assert same_ideal(degree_two, remainders, a)

        transfer_basis = reduced_basis(equations, a)
        standards = standard_exponents_zero_dimensional(transfer_basis)
        assert len(standards) == 2**k

        remainder_basis = reduced_basis(remainders, a)
        hilbert = monomial_hilbert_series(remainder_basis)
        high_start = (k + 1) // 2
        # Setting the low coefficients to zero kills every convolution
        # equation and leaves these variables polynomially free.
        substitution = {a[j]: 0 for j in range(high_start)}
        assert all(sp.expand(f.subs(substitution)) == 0 for f in remainders)

        verdict = "agrees" if k == 1 else "fails (positive-dimensional proposed fiber)"
        print(
            f"k={k}: degree-2 component ideal=rem(A^2); "
            f"transfer length={len(standards)}; H_C={hilbert}; {verdict}"
        )

        if k <= 4:
            names = [format_monomial(e, a) for e in standards]
            print(f"  exact collided transfer basis: {names}")

        # Exact bounded depth certificates used in the note.  Equality
        # H_(R/(l))(t)=(1-t)H_R(t) proves that l is a nonzerodivisor via the
        # graded multiplication exact sequence.  The selected sequences are
        # not extrapolated beyond the tested cases.
        certified_depth = {3: 1, 4: 1, 5: 1, 6: 2}.get(k, 0)
        if certified_depth:
            linear_forms: tuple[sp.Symbol, ...] = ()
            previous_hilbert = hilbert
            for j in range(certified_depth):
                linear_forms += (a[k - 1 - j],)
                quotient_basis = reduced_basis(remainders + linear_forms, a)
                quotient_hilbert = monomial_hilbert_series(quotient_basis)
                assert sp.cancel(quotient_hilbert - (1 - t) * previous_hilbert) == 0
                previous_hilbert = quotient_hilbert

    # The decisive universal k=2 certificate.
    base, a, _, _, _, _, equations, remainders = presentation(2, collided=False)
    s1, s2 = base
    Y, X = a  # A=X*Z+Y
    domain = sp.QQ.poly_ring(s1, s2)
    expected_transfer = (X**3, 2 * X * Y - s1 * X**2, Y**2 - s2 * X**2)
    assert same_ideal(equations, expected_transfer, a, domain)
    discriminant = s1**2 - 4 * s2
    g1, g2 = remainders[1], remainders[0]
    assert sp.expand(discriminant * X**3 - (-2 * Y * g1 + 4 * X * g2 - s1 * X * g1)) == 0
    special_quadratic = reduced_basis(
        tuple(f.subs({s1: 0, s2: 0}) for f in remainders), a
    )
    assert special_quadratic.reduce(X**3)[1] != 0
    print("k=2 certificate: in_a(I) adds X^3; (s1^2-4*s2)X^3=0 in C_2, X^3!=0")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--max-k", type=int, default=6)
    args = parser.parse_args()
    audit(args.max_k)


if __name__ == "__main__":
    main()
